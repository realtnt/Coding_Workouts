def to_mountain(mat):
    for i, v in enumerate(mat):
        pass

    return mat


print(to_mountain([[1]]))  # , [[1]])
print(to_mountain([[1, 1]]))  # , [[1, 1]])
print(to_mountain([[1, 2]]))  # , [[1, 2]])
print(to_mountain([[1, 3]]))  # , [[2, 3]])
print(to_mountain([[2, 2, 1, 2],
                  [1, 0, 2, 1],
                  [1, 0, 1, 2],
                  [1, 2, 2, 1],
                   ]))
'''
    [[2, 2, 1, 2],
    [1, 1, 2, 1],
    [1, 1, 1, 2],
    [1, 2, 2, 1]]) 
    '''
print(to_mountain([[2, 2, 1, 2],
                  [1, 0, 2, 1],
                  [1, 0, 1, 2],
                  [1, 2, 2, 4]]))
'''
    [[2, 2, 1, 2],
    [1, 2, 2, 2],
    [1, 2, 3, 3],
    [1, 2, 3, 4]])
    '''
print(to_mountain([[2, 2, 1, 2],
                  [1, 0, 2, 1],
                  [1, 0, 4, 2],
                  [1, 2, 2, 4]]))
'''
    [[2, 2, 2, 2],
    [2, 3, 3, 3],
    [2, 3, 4, 3],
    [2, 3, 3, 4]])
    '''
print(to_mountain([[1, 2, 4, 2],
                  [3, 3, 3, 4],
                  [1, 2, 4, 2],
                  [4, 4, 1, 3]]))
